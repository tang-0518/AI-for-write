def expand_triggers(keywords: List[str]) -> Set[str]:
    """将触发词列表展开为扩展检索词集合

    Args:
        keywords: 触发词列表，例如 ["战斗", "修炼"]

    Returns:
        所有触发词对应的扩展词并集；若某触发词不在目录中，
        则将其自身加入结果集作为兜底。
    """
    result: Set[str] = set()
    for kw in keywords:
        expanded = _TRIGGER_CATALOG.get(kw)
        if expanded:
            result |= expanded
        else:
            # 未知触发词：原词兜底，保证召回不丢失
            result.add(kw)
    return result
